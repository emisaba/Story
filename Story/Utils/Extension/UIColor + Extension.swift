import UIKit

extension UIColor {
    static func customGreen() -> UIColor {
        return UIColor(red: 6/255, green: 57/255, blue: 57/255, alpha: 1)
    }
    
    static func customLightGreen() -> UIColor {
        return UIColor(red: 66/255, green: 97/255, blue: 97/255, alpha: 1)
    }
    
    static func customYellow() -> UIColor {
        return UIColor(red: 251/255, green: 177/255, blue: 0/255, alpha: 1)
    }
    
    static func customOrange() -> UIColor {
        return UIColor(red: 219/255, green: 164/255, blue: 143/255, alpha: 1)
    }
    
    static func customLightOrange() -> UIColor {
        return UIColor(red: 247/255, green: 208/255, blue: 169/255, alpha: 1)
    }
}
